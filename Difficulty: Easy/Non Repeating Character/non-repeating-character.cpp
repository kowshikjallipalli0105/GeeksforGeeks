class Solution {
  public:
    // Function to find the first non-repeating character in a string.
    char nonRepeatingChar(string &s) {
        // Your code here
        map<char,int>m;
        for(auto i:s){
            m[i]++;
        }
        // char ans= '$';
        for(auto i:s){
            if(m[i]==1){
                return i;
            }
        }
        return '$';
    }
};

