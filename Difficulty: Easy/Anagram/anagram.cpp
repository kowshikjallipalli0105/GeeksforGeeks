class Solution {
  public:
    // Function is to check whether two strings are anagram of each other or not.
    bool areAnagrams(string& s1, string& s2) {
        // Your code here
        map<char,int>m1;
        map<char,int>m2;
        for(auto i:s1){
            m1[i]++;
        }
        for(auto i:s2){
            m2[i]++;
        }
        if(m1!=m2) return false;
        return true;
    }
};

